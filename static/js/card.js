function changeBank(type) {
    let judgementImage = document.getElementById('judgement_image');
    let selectionImage1 = document.getElementById('selection_image1');
    let selectionImage2 = document.getElementById('selection_image2');

    let judgementQText = document.getElementById('jud_Qtext');
    // let judgementPromptText = document.getElementById('jud-prompt');
    let selectionQText = document.getElementById('sel_Qtext');
    // let selectionPromptText = document.getElementById('sel-prompt');

    switch(type) {
        case 'bank0':
            judgementImage.src = 'static/img/LLM-exam/judgement/Crab_Real.jpg';
            selectionImage1.src = 'static/img/LLM-exam/selection/dog_fake.jpg';
            selectionImage2.src = 'static/img/LLM-exam/selection/dog_real.jpg';
            judgementQText.innerHTML = '"You have been shown one image, possibly generated by AI. <strong>Please identify whether this image is generated by AI or not</strong>."';
            selectionQText.innerHTML = '"You have been shown two different images. One of the images is not generated by AI. <strong>Which of the above images is most likely not generated by AI ?</strong>"';
            break;
        case 'bank1':
            judgementImage.src = 'static/img/LLM-exam/judgement/person_real.jpg';
            selectionImage1.src = 'static/img/LLM-exam/selection/person_real.jpg';
            selectionImage2.src = 'static/img/LLM-exam/selection/person_fake.jpg';
            judgementQText.innerHTML = '"You have been shown one image, possibly generated by AI. <strong>Please identify whether this image is generated by AI or not</strong>."';
            selectionQText.innerHTML = '"You have been shown two different images. One of the images is not generated by AI. <strong>Which of the above images is most likely not generated by AI ?</strong>"';
            break;
        case 'bank2':
            judgementImage.src = 'static/img/LLM-exam/judgement/3D_real.jpg';
            selectionImage1.src = 'static/img/LLM-exam/selection/3D_fake.jpg';
            selectionImage2.src = 'static/img/LLM-exam/selection/3D_real.jpg';
            judgementQText.innerHTML = '"You have been shown one image, possibly generated by AI. <strong>Please identify whether this image is generated by AI or not</strong>."';
            selectionQText.innerHTML = '"You have been shown two different images. One of the images is not generated by AI. <strong>Which of the above images is most likely not generated by AI ?</strong>"';
            break;
        case 'bank3':
            judgementImage.src = 'static/img/LLM-exam/judgement/text_fake.jpg';
            selectionImage1.src = 'static/img/LLM-exam/selection/Text_real.jpg';
            selectionImage2.src = 'static/img/LLM-exam/selection/Text_fake.jpg';
            judgementQText.innerHTML = '"You have been shown one passage. This passage may be generated by a LLM or written by real people. <strong>Please identify whether this passage is generated by LLM or not.</strong>"';
            selectionQText.innerHTML = '"You have been shown two different passages. One of the passages is generated by a LLM, while the other is written by real people. <strong>Which of the above passages is most likely generated by AI?</strong>"';
            break;
    }
}

