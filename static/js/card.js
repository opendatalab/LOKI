let currentQuiz = 'bank0'; // default type

function setQuizType(type) {
    currentQuiz = type;
    changeBank(type);
}

function checkAnswer(cardType, answer) {
    const correctAnswer = answerKey[currentQuiz][cardType];
    if (cardType == 'judgement') { /* judgement type */
        // get elements
        let judgementImage = document.getElementById('judgement_image');
        let judgementQText = document.getElementById('jud_Qtext');
        let buttonText = document.getElementById('jud-prompt');

        judgementImage.src = answerKey[currentQuiz][cardType + '_image'];
        // buttonText.innerText = '';
        if (answer === correctAnswer) {
            judgementQText.innerHTML = '<strong style="font-size:25px; color:green">Correct!</strong>';
        } else {
            judgementQText.innerHTML = '<strong style="font-size:25px; color:red">Incorrect!</strong>';
        }
    } else { /* selection type */
        // get elements
        let selectionImage1 = document.getElementById('selection_image1');
        let selectionImage2 = document.getElementById('selection_image2');
        let selectionQText = document.getElementById('sel_Qtext');
        let buttonText = document.getElementById('sel-prompt');

        selectionImage1.src = answerKey[currentQuiz][cardType + '_image1'];
        selectionImage2.src = answerKey[currentQuiz][cardType + '_image2'];
        // buttonText.innerText = '';
        if (answer === correctAnswer) {
            selectionQText.innerHTML = '<strong style="font-size:25px; color:green">Correct!</strong>';
            
        } else {
            selectionQText.innerHTML = '<strong style="font-size:25px; color:red">Incorrect!</strong>';
        }
    }
}